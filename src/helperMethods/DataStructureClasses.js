export class ListNode {
    constructor(value){
        this.value = value
        this.next = null
        this.prev = null
    }
}


export class TreeNode {
    constructor(value) {
        this.value = value;
        this.left = null;
        this.right = null;
        this.frequency = 0;
        this.x = 0;
        this.y = 0;
        this.nodeNumberOnBranch = 0;
    }
}

export class HeapNode {
    constructor(value) {
        this.value = value;
        this.left = null;
        this.right = null; 
    }
}
